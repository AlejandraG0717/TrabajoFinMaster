////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Functions associated to STATUS topic (publisher).
// Publish PVSx//MAC every power on or when requested
// Publish configuration parameters (whe asked in config)
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "CONFIG.h"
#include <Preferences.h>

extern Preferences p;
extern String PVSx;

void load_params() {
    p.begin("CONFIG", false);
    if (!p.isKey("PVSx_NV")) {
        p.putString("PVSx_NV", PVSx);
    }
    PVSx = p.getString("PVSx_NV", PVSx);
    p.end();
}
