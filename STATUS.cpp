////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Functions associated to STATUS topic (publisher).
// Publish PVSx//MAC every power on or when requested
// Publish configuration parameters (whe asked in config)
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "STATUS.h"
#include <PubSubClient.h>

#include <WiFi.h> 
#include <PubSubClient.h> 
extern PubSubClient client; 
extern String PVSx; 
extern String uC_name; 
extern const char* TOPIC_STATUS;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Publish status (PVSx//MAC). First time IN EMPTY/STATUS, then PVSx/STATUS.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void publishStatusAvailable() {
    String topic = PVSx + "/" + TOPIC_STATUS; 
    String msg = PVSx + "//" + uC_name + "//Available"; 
    // Usar mesh_publicar para que funcione en todos los modos
    extern void mesh_publicar(const String& topic, const String& payload);
    mesh_publicar(topic, msg);
}
